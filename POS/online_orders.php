// Fetch Orders
$orders = [];
$orders_query = mysqli_query($conn, "SELECT * FROM tbl_orders WHERE delivery_instructions != 'POS Transaction' OR delivery_instructions IS NULL ORDER BY order_date DESC");
if ($orders_query) {
    while ($row = mysqli_fetch_assoc($orders_query)) {
        $order_id = $row['order_id'];
        $raw_type = strtolower($row['delivery_type'] ?: 'delivery');
        $type_display = ($raw_type === 'pickup' || $raw_type === 'pick up') ? 'Pick Up' : 'Delivery';
        
        $orders[$order_id] = [
            'id' => $order_id,
            'customer' => 'Customer #' . $order_id,
            'platform' => 'Website',
            'type' => $type_display,
            'price' => floatval($row['total_amount']),
            'status' => ucfirst($row['order_status'] ?: 'Pending'),
            'time' => date('M j, g:i A', strtotime($row['order_date'])),
            'payment_method' => $row['payment_method'],
            'delivery_instructions' => $row['delivery_instructions'],
            'items' => []
        ];
    }
}

// Fetch Items
$items_query = mysqli_query($conn, "
    SELECT oi.order_id, oi.quantity, oi.price_at_time, p.prod_name, p.prod_image 
    FROM tbl_order_items oi 
    LEFT JOIN tbl_products p ON oi.prod_id = p.prod_id
");
if ($items_query) {
    while ($row = mysqli_fetch_assoc($items_query)) {
        $order_id = $row['order_id'];
        if (isset($orders[$order_id])) {
            $orders[$order_id]['items'][] = [
                'name' => $row['prod_name'] ? $row['prod_name'] : 'Unknown Product',
                'quantity' => intval($row['quantity']),
                'price' => floatval($row['price_at_time']),
                'image' => $row['prod_image']
            ];
        }
    }
}

